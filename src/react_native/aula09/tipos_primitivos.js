/* 
  Tipos primitivos do JavaScript 
  - undefined : representa tipo indefinido
  - Boolean   : representa true ou false
  - Number    : representa os números inteiros ou de ponto flutuante
  - String    : representa caracteres delimitados por ' ou "
  - Object    : representa objetos ou arrays
  - Function  : representa funções
 */
console.log(typeof(undefined));
console.log(typeof(true));
console.log(typeof(10.123));
console.log(typeof('u'));
console.log(typeof('um texto qualquer'));
console.log(typeof([1, 2, 3]));
console.log(typeof({ id: 1, nome: 'Jose' }));
console.log(typeof(alert));
