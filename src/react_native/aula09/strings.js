// string com aspas simples
let str1 = 'Esta é uma string';
console.log(str1);

// string com aspas duplas
let str2 = "Esta é outra string";
console.log(str2);

// permite combinar aspas simples e duplas
let str3 = ' " Olá Mundo! " ';
console.log(str3);
let str4 = " ' Olá Mundo! ' ";
console.log(str4);

// permite caracteres especiais
let str5 = 'Olá \n Mundo!';
console.log(str5);

// permite expressões embutidas
let str6 = `A mensagem é ${str3}`;
console.log(str6);
let str7 = `A soma de 2 e 2 é ${2 + 2}`;
console.log(str7);

// tamanho da string
console.log(str1.length);
